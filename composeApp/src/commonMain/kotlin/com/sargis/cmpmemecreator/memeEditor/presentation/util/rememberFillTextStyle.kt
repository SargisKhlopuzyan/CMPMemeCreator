package com.sargis.cmpmemecreator.memeEditor.presentation.util

import androidx.compose.runtime.Composable
import androidx.compose.runtime.remember
import androidx.compose.ui.graphics.Color
import androidx.compose.ui.text.TextStyle
import androidx.compose.ui.text.font.FontFamily
import androidx.compose.ui.text.style.TextAlign
import androidx.compose.ui.unit.TextUnit
import androidx.compose.ui.unit.sp
import com.sargis.cmpmemecreator.core.theme2.Fonts

@Composable
fun rememberFillTextStyle(
    fontSize: TextUnit = 36.sp,
    fontFamily: FontFamily = Fonts.Impact,
    fillColor: Color = Color.White,
    textAlign: TextAlign = TextAlign.Center
): TextStyle {
    return remember(fontSize, fontFamily, fillColor, textAlign) {
        TextStyle(
            color = fillColor,
            fontFamily = fontFamily,
            textAlign = textAlign,
            fontSize = fontSize
        )
    }
}